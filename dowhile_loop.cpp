#include<iostream>
using namespace std;

int main(){
    int i=1;

    // In do while loop, condition is given in last of the loop
    // condition will check after one iteration(cycle)
    do{
        cout<<i<<endl;
        i++;
    }while(false);



    do{
        cout << i << endl;
        i++;

    }while(false);
    return 0;
}
